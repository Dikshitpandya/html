winding and electrical  engineering requires knowledge in various 
complex technology fields, such as –  Magnetic field, stray flux, eddy 
currents, loss generation, temp rise, hot  spot, surface oxidation, 
internal &amp; external cooling, hydraulics,  mechanical design, short 
circuit, noise, transport, electric network, impulse  tests, voltage 
transients, resonance, dielectric design, etc. The technology of  
winding material and electrical engineering therefore, demands knowledge
 of  finer points of these fields.</p>
