look at the Inter-tie electrical winding, its  tertiary windings, though
 have low voltage, low rating – often suffer from  short circuit 
problems. On the other hand, the LV-HV windings have high stray  flux – 
generating eddy losses and hot-spot problems. Also have high voltage – 
resulting  in dielectric problems especially for impulse testing. It is 
difficult to make  them short-circuit-proof, especially the inner common
 winding which is prone to  buckling failures. Depending on type of 
regulation and consequently on its  position, the winding may cause 
severe short circuit problems. MADHAV COPPER  offers confident solutions
 to electrical winding designers to overcome all such  issues.</p>
